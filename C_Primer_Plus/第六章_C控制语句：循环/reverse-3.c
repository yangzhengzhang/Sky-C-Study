#include <stdio.h>

int main(void)
{
    char string[40];
    int i;

    printf("Please print a word: ");
    scanf("%s", string);
    if (strlen(string) > 40)
        printf("Sorry, too long");
    else
        for (i = strlen(string) - 1; i >= 0 ; i--)
            printf("%c", string[i]);
    printf("\n");
    return 0;
}
